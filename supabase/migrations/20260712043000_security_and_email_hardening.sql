-- Durable per-user rate-limit ledger for the authenticated Gemini proxy.
CREATE TABLE IF NOT EXISTS public.gemini_proxy_usage (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  created_at timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_gemini_proxy_usage_user_created
  ON public.gemini_proxy_usage(user_id, created_at DESC);
ALTER TABLE public.gemini_proxy_usage ENABLE ROW LEVEL SECURITY;
REVOKE ALL ON TABLE public.gemini_proxy_usage FROM anon, authenticated;
GRANT ALL ON TABLE public.gemini_proxy_usage TO service_role;
GRANT USAGE, SELECT ON SEQUENCE public.gemini_proxy_usage_id_seq TO service_role;

CREATE OR REPLACE FUNCTION public.consume_gemini_proxy_quota(
  _user_id uuid,
  _limit integer DEFAULT 10
) RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  recent_count integer;
BEGIN
  IF _user_id IS NULL OR _limit < 1 OR _limit > 60 THEN
    RETURN false;
  END IF;

  PERFORM pg_advisory_xact_lock(hashtext(_user_id::text));
  DELETE FROM public.gemini_proxy_usage
    WHERE created_at < now() - interval '1 hour';
  SELECT count(*) INTO recent_count
    FROM public.gemini_proxy_usage
    WHERE user_id = _user_id
      AND created_at >= now() - interval '1 minute';
  IF recent_count >= _limit THEN
    RETURN false;
  END IF;
  INSERT INTO public.gemini_proxy_usage(user_id) VALUES (_user_id);
  RETURN true;
END;
$$;
REVOKE ALL ON FUNCTION public.consume_gemini_proxy_quota(uuid, integer)
  FROM PUBLIC, anon, authenticated;
GRANT EXECUTE ON FUNCTION public.consume_gemini_proxy_quota(uuid, integer)
  TO service_role;

