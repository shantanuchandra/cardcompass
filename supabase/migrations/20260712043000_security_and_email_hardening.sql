ALTER FUNCTION public.get_user_cards(uuid) SECURITY INVOKER SET search_path = public;
ALTER FUNCTION public.get_user_transactions(uuid, integer) SECURITY INVOKER SET search_path = public;
ALTER FUNCTION public.remove_user_card(uuid, uuid) SECURITY INVOKER SET search_path = public;
ALTER FUNCTION public.update_user_card(uuid, uuid, text, numeric, text, text, integer, integer) SECURITY INVOKER SET search_path = public;

